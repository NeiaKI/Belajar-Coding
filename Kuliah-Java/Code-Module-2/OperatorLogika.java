public class OperatorLogika {
    public static void main(String[] args) {
    int x = 10, y = 20;
    System.out.println("x == y : " + (x == y));
    System.out.println("x != y : " + (x != y));
    System.out.println("x > y : " + (x > y));
    System.out.println("x < y : " + (x < y));
    boolean p = true, q = false;
    System.out.println("p && q : " + (p && q));
    System.out.println("p || q : " + (p || q));
    System.out.println("!p : " + (!p));
    }
}