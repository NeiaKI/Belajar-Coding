public class ContohFor {
    public static void main(String[] args) {
        System.out.println("Angka Genap dari 2 hingga 10:");
        // Inisialisasi: int i = 2
        // Kondisi: i <= 10
        // Perubahan: i += 2 (Menambahkan 2 di setiap iterasi)
        for (int i = 2; i <= 10; i += 2) {
            System.out.println(i);
        }
    }
}